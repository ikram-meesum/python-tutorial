# The end parameter in Python is a keyword argument primarily used with the built-in
# print() function. It controls the characters printed at the end of the output of a
# print() statement

print("Python", end='@')
print("Google")


# Seprating with Comma
print('A', 'B', 'C')
print('A', 'B', 'C', sep='')

# for formatting a date
print('09', '12', '2026')
print('09', '12', '2026', sep='-')
