# Print 0 to 9 using range function
for i in range(10):
    print(i)


# Start from 1 to 9
for i in range(1, 10):
    print(i)

# Start from 1 and increment by 2
for x in range(1, 10, 2):
    print(x)

# Information after end of loop
for x in range(6):
    print(x)
else:
    print("Finally finished!")

# loop on string value
for x in 'banana':
    print(x)
