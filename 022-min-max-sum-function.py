num = [27, 32, 73, 45, 36, 17, 85, 90]
print(f"Minimum value of list is {min(num)}")
print(f"Maximum value of list is {max(num)}")
print(f"The sum of list is {sum(num)}")

# Round function
x = 3.15
result = round(x)
print(result)

# Absolute function
y = -5
result = abs(y)
print(result)

# Power function
print(pow(4, 3))

# Min and max function

a = 36
b = 45
c = 27

res1 = max(a, b, c)
print('Max value: ', res1)

res2 = min(a, b, c)
print('Min value:', res2)
